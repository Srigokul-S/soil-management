window.translations = translations;

// Translation function
function translate(key, lang = 'English') {
    if (!translations[lang]) {
        lang = 'English';
    }
    return translations[lang][key] || key;
}

// Update page text with translations
function updatePageTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translated = translate(key, lang);
        
        if (element.tagName === 'OPTION' || element.tagName === 'INPUT' || element.tagName === 'SELECT') {
            element.textContent = translated;
        } else {
            element.textContent = translated;
        }
    });
    
    // Update document language
    document.documentElement.lang = lang.toLowerCase().replace(' ', '-');
}

// Language selector event listener
document.addEventListener('DOMContentLoaded', function() {
    const languageSelector = document.getElementById('language');
    
    if (languageSelector) {
        languageSelector.addEventListener('change', function() {
            const selectedLang = this.value;
            updatePageTranslations(selectedLang);
            localStorage.setItem('preferredLanguage', selectedLang);
        });
        
        // Load saved language preference
        const savedLang = localStorage.getItem('preferredLanguage') || 'English';
        languageSelector.value = savedLang;
        updatePageTranslations(savedLang);
    }
});

window.translate = translate;
window.updatePageTranslations = updatePageTranslations;
